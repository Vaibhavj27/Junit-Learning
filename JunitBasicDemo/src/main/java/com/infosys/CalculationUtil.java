package com.infosys;

public class CalculationUtil {
    public int add(int a,int b){
        return a+b;
    }

    public int multiply(int a , int b)
    {
        return a*b;
    }

    public String parseString(String input){
        return input;
    }

    public int Division(int a,int b){
        return a/b;
    }

    public Boolean isEven(int a)
    {
        return (a%2==0);
    }
}
